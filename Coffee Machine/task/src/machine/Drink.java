package machine;

public class Drink {
    int waterPerCup;
    int milkPerCup;
    int coffeePerCup;
    int costPerCup;

    public Drink(int waterPerCup, int milkPerCup, int coffeePerCup, int costPerCup) {
        this.waterPerCup = waterPerCup;
        this.milkPerCup = milkPerCup;
        this.coffeePerCup = coffeePerCup;
        this.costPerCup = costPerCup;
    }
}
